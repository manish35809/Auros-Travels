    void load_basic_data(){
        try {
            String qry_1 = "SELECT * FROM USERS WHERE User_Name = '"+user+"'";
            ResultSet basic_data = c.get_data(qry_1);
            if(basic_data.next()){
                String fname = basic_data.getString("Full_Name");
                long number = basic_data.getLong("Mobile_Number");
                String mail = basic_data.getString("E_Mail");
                String pass  = basic_data.getString("Password");
                
                full_name.setText(fname);
                mobile_number.setText(number+"");
                email.setText(mail);
                password.setText(pass);
                user_name.setText(user);
            }
            else{
                c.message("Unable to Load Basic Data");
            }
        } catch (ClassNotFoundException | SQLException ex) {
            Logger.getLogger(Profile.class.getName()).log(Level.SEVERE, null, ex);
        }
        
    }
    
    void load_card_data(){
        try {
            String qry = "SELECT * FROM card_details WHERE User_Name = '"+user+"'";
            ResultSet card_data = c.get_data(qry);
            if(card_data.next()){
                long cno = card_data.getLong("Account_Number"); 
                int cvv = card_data.getInt("CVV");
                int month = card_data.getInt("Month");
                int year = card_data.getInt("Year");
                
                acnof.setText(""+cno);
                cvvf.setText(""+cvv);
                mf.setText(""+month);
                yf.setText(""+year);
            }
            else{
                c.message("Card Data Not Available.");
                card_details.setVisible(false);
            }
        } catch (ClassNotFoundException | SQLException ex) {
            Logger.getLogger(Profile.class.getName()).log(Level.SEVERE, null, ex);
        }
        
    }
    
