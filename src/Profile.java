    private void basic_updateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_basic_updateActionPerformed
        String new_name = full_name.getText();
        double new_number = Double.parseDouble(mobile_number.getText());
        String new_mail = email.getText();
        String new_pass = password.getText();
        
        if(new_name.length() == 0 || new_mail.length() == 0 || new_pass.length() == 0 || new_number == 0){
            c.message("Please Fill All Fields.");
        }
        else{
            String qry = "UPDATE USERS SET Full_Name = '"+new_name+"' , Mobile_Number = "+new_number+" , E_Mail = '"+new_mail+"' , Password = '"+new_pass+"'";
            try {
                int res = c.update(qry);
                if(res == 0){
                    c.message("Unable To Update Details.");
                }
                else{
                    c.message("Details Update Successfully.");
                    full_name.setText("");
                    mobile_number.setText("");
                    email.setText("");
                    user_name.setText("");
                    password.setText("");
                }
            } catch (ClassNotFoundException | SQLException ex) {
                Logger.getLogger(Profile.class.getName()).log(Level.SEVERE, null, ex);
            }
        }
        
    }//GEN-LAST:event_basic_updateActionPerformed

