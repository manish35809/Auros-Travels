    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        double amt = 0;
        int id = Integer.parseInt(id_tf.getText());
        if(Integer.toString(id).length() == 0 || Integer.toString(id).equals("Booking_ID")){
            c.message("Please Fill the Correct Value in Field");
        }
        else{
            String get_qry = "SELECT Tickets_Total_Amount FROM tickets WHERE ID = "+id;
            String del_qry = "DELETE FROM tickets WHERE ID ="+id;
            int response = JOptionPane.showConfirmDialog(null, "Do you Really want to Cancel Your Ticket ?");
            if(response == 0){
                try {
                    ResultSet get_res = c.get_data(get_qry);
                    while(get_res.next()){
                        amt = get_res.getDouble("Tickets_Total_Amount");
                    }
                    String add_qry = "UPDATE users SET Money = Money + "+amt+" WHERE User_Name = '"+user+"'";
                    int add_res = c.update(add_qry);
                    if(add_res == 0){
                        c.message("Unable to Add Money to Wallet");
                    }
                    else{
                        int del_res = c.update(del_qry);
                        if(del_res == 0){
                            c.message("Unable to Cancel the Booking.");
                        }
                        else{
                            c.message("Booking Canceled Successfully.");
                            String qry_2 = "INSERT INTO transactions(User_Name,Amount,Info,Date_Time) VALUES ('"+user+"',"+amt+",'Booking Cancelled Money Added',now())";
                            int res_2 = c.update(qry_2);
                            if(res_2 == 1){
                                c.message("Transaction Saved Successfully.");
                            }
                            else{
                                c.message("Unable to Save Transaction.");
                            }
                            load_bookings();
                            id_tf.setText("Booking_ID");
                        }
                    }
                } catch (ClassNotFoundException | SQLException ex) {
                    Logger.getLogger(My_Bookings.class.getName()).log(Level.SEVERE, null, ex);
                }            
            }
            else{}
        }
        
    }//GEN-LAST:event_jButton1ActionPerformed

