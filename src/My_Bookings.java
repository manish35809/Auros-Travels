    void load_bookings(){
        DefaultTableModel model = (DefaultTableModel) booking_table.getModel();
        model.setRowCount(0);
        String qry = "SELECT ID,T_Date,Source,Destination,Ticket_Class,Tickets_Total_Amount from tickets WHERE User_Name = '"+user+"'";
        try {
            ResultSet bookings = c.get_data(qry);
            while(bookings.next()){
                int id = bookings.getInt("ID");
                String t_date = bookings.getDate("T_DATE").toString();
                String t_source = bookings.getString("Source");
                String t_dest = bookings.getString("Destination");
                String tk_class = bookings.getString("Ticket_Class");
                double trip_amt = bookings.getDouble("Tickets_Total_Amount");
                Object[] row = {id,t_date,t_source,t_dest,tk_class,trip_amt};
                model.addRow(row);
            }
        } catch (ClassNotFoundException | SQLException ex) {
            Logger.getLogger(My_Bookings.class.getName()).log(Level.SEVERE, null, ex);
        }
    }
    
